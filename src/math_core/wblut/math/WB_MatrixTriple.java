package wblut.math;

/**
 *
 */
public class WB_MatrixTriple {
	/**  */
	public int i; // row
	/**  */
	public int j; // col
	/**  */
	public double value;

	/**
	 *
	 *
	 * @param i
	 * @param j
	 * @param value
	 */
	public WB_MatrixTriple(final int i, final int j, final double value) {
		this.i = i;
		this.j = j;
		this.value = value;
	}
}
